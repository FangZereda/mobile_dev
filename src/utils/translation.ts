export const translateProduct = (product: any): any => {
  const categoryKey = product.category as string;
  const categoryTranslation = translations[categoryKey] || {};

  return {
    ...product,
    title: translateText(product.title),
    description: translateText(product.description),
    brand: translateText(product.brand),
    category: categoryTranslation.category || translateText(product.category),
  };
};

export const translateCategory = (category: string): string => {
  const translation = translations[category];
  return translation?.title || translateText(category);
};
